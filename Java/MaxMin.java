import java.lang.Math;
import java.util.*;

public class MaxMin{
     public static void main(String[] args) {
     int a,b;
     Scanner sc = new Scanner(System.in);
     a = sc.nextInt();
     b = sc.nextInt();
     System.out.println("The maximum is "+ Math.max(a,b) );
     System.out.println("The minimum is "+ Math.min(a,b) );
sc.close();

}
}